// Trier (ASC et DESC) le tableau suivant:

const data = [
    'PHP',
    'Java',
    'Go',
    'C',
    'Python',
    'JavaScript',
    'Symfony',
    'Angular',
];

// TRIER = SORT
data.sort(); // sort(): tri les élémenrs d'un tableau par ordre alphabétique
console.log(data); // Angular', 'C', 'Go', 'Java', 'JavaScript', 'PHP', 'Python', 'Symfony'

// INVERSE = REVERSE
data.reverse(); // reverse(): tri les éléments d'un tableau par ordre alphabétique inversé
console.log(data); // 'Symfony', 'Python', 'PHP', 'JavaScript', 'Java', 'Go', 'C', 'Angular'